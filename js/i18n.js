function translate(root = document) {
    const lang = localStorage.getItem('lang') || 'zh';
    const elements = root.querySelectorAll('[data-i18n]');
    elements.forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            const value = translations[lang][key];
            if (value.includes('<')) {
                el.innerHTML = value;
            } else {
                el.textContent = value;
            }
        }
    });

    // Translate placeholders
    const placeholderElements = root.querySelectorAll('[data-i18n-placeholder]');
    placeholderElements.forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        if (translations[lang] && translations[lang][key]) {
            el.placeholder = translations[lang][key];
        }
    });
}

function setLanguage(lang) {
    document.documentElement.lang = lang === 'zh' ? 'zh-CN' : 'en-US';
    localStorage.setItem('lang', lang);
    
    // Update active state of language selector if it exists
    const langToggle = document.getElementById('lang-toggle');
    if (langToggle) {
        const langLabel = langToggle.querySelector('.lang-label');
        if (langLabel) {
            langLabel.textContent = translations[lang]['footer.lang'];
        }
    }

    translate();

    // Trigger custom event for other scripts to react (e.g., article re-rendering)
    window.dispatchEvent(new CustomEvent('languageChanged', { detail: { lang } }));
}

async function initLanguage() {
    const savedLang = localStorage.getItem('lang');
    if (savedLang) {
        setLanguage(savedLang);
        return;
    }

    // Default from browser locale only; avoid third-party IP lookup.
    const navLang = navigator.language || navigator.userLanguage || 'en';
    const lang = navLang.toLowerCase().includes('zh') ? 'zh' : 'en';
    setLanguage(lang);
}

// Expose to window
window.translations = translations;
window.setLanguage = setLanguage;
window.initLanguage = initLanguage;
window.translate = translate;
